#include "3-calc.h"

/**
 * op_add - sum operation
 * @a: number to be sum
 * @b: number to be sum
 * Return: sum total
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - subtract operation
 * @a: number to be substarct
 * @b: number to be substarct
 * Return: substraction total
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiplication operation
 * @a: number to be multiplied
 * @b: number to be multiplied
 * Return: multiplication total
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - division operation
 * @a: number to be divided
 * @b: number to be divided
 * Return: division operation total
 */

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - modulus operation
 * @a: number to be evaluated
 * @b: number to be evaluated
 * Return:mod operation total.
 */

int op_mod(int a, int b)
{
	return (a % b);
}
